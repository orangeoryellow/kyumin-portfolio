#include <stdio.h>

// Write a program to add 3 numbes
// Ask user for 3 different numbers using scanf
// scanf("%i", ...);
// Then printf the sum of the three numbers

int main() { 
	int length, width, area;
	printf("Enter length: ");
	scanf("%i", &length);
	printf("Enter width: ");
	scanf("%i", &width);
	printf("Enter area: ");
	scanf("%i", &area);
	printf("The sum of the three number is %i\n", length * width = area);
	return 0;
}
